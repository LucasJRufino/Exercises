package calculadoradomitor;

public class Contas {

    public static float Somar(float sum1, float sum2) {
        return sum1 + sum2;
    }

    public static float Subtrair(float min1, float min2) {
        return min1 - min2;
    }

    public static float Multiplicar(float mult1, float mult2) {
        return mult1 * mult2;
    }

    public static float Dividir(float div1, float div2) {
        return div1 / div2;
    }
}
